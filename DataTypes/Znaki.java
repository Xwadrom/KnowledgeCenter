public class Znaki {
    public static void main(String[] args) {
        System.out.println('a');
        System.out.println('b');// pojedyńcze znaki piszemy w apostrofie
        System.out.println("ciąg znaków czyli string");
        System.out.println('\u2602');// kod ASCII
        System.out.println('a'+'b');// dodaje kod znakow w ASCII
        System.out.println(2+2);
        System.out.println("Ala " + "ma " + "kota");
        System.out.println("Ala\n ma \n kota");
        System.out.print("Liczba int:");//max 2 do potegi 31
        System.out.println(25);
        System.out.print("Liczba double:");// max 16 liczb po przecinku, większa dokładność niż float ktory ma 8
        System.out.println(25.75);
        System.out.print("Czy uczę się programowania? ");
        System.out.println(true);


    }
}

